const add = (a, b) => {
    return a + b;
};
const subtract = (a, b) => {
    return a - b;
};
const multiply  = (a, b) => {
    return a * b;
};
const division = (a, b) => {
    return a / b;
};

export default { add, subtract, multiply, division }; // Export the `add` function as part of the default object
